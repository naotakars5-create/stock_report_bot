"""
promo/ng_words.py

広報テキスト（X投稿・note下書き）に対する **禁止語チェック**。

本サービスは投資助言ではなく、公開データに基づく機械的なスクリーニングです。
売買を推奨・示唆する表現は、LINE配信と同様に広報でも使いません。

誤検知への配慮（重要）:
  素朴な部分一致だと、以下の **正当な表現** まで弾いてしまう。
    - 免責文「特定銘柄の売買を**推奨**するものではありません」 → 「推奨」に一致
    - 適時開示の事実「自社株**買い**を開示」            → 「買い」に一致
  そのため、チェック前に ALLOWED_PHRASES を除去してから走査する。
  （＝「推奨しない」と明示する文や、開示種別の固有名詞は許容する）
"""

# 使ってはいけない語（売買の推奨・示唆と受け取られ得る表現）
NG_WORDS = [
    "買い", "売り", "推奨", "エントリー", "利確", "損切り", "狙い目", "仕込み",
    "チャンス", "おすすめ", "買い候補", "今後伸びる", "上昇予想", "想定上昇率",
    "儲か", "強気", "弱気", "勝てる", "利益が出る", "買われ", "上がる", "押し目",
]

# 誤検知を避けるため、走査前に取り除く「正当な表現」
#   - 免責文: 「推奨しない」と明示する文なので許容
#   - 適時開示の種別名: 公開事実の固有名詞なので許容
ALLOWED_PHRASES = [
    "特定銘柄の売買を推奨するものではありません",
    "売買を推奨するものではありません",
    "売買推奨ではありません",
    "売買の推奨ではありません",
    "推奨するものではありません",
    "自社株買い",
]


def check_ng(text):
    """
    テキストに含まれる禁止語を返す（重複なし・昇順）。問題なければ空リスト。

    ALLOWED_PHRASES（免責文・開示種別名）は走査前に除去する。
    """
    if not text:
        return []
    scrubbed = str(text)
    for phrase in ALLOWED_PHRASES:
        scrubbed = scrubbed.replace(phrase, "")
    return sorted({w for w in NG_WORDS if w in scrubbed})


def is_clean(text):
    """禁止語を含まなければ True。"""
    return not check_ng(text)


def assert_clean(text, label="text"):
    """
    禁止語を含む場合に ValueError を送出する（生成関数の自己防衛用）。

    投稿直前のガードとして使う。検出時は機械的に置換せず、呼び出し側で
    ログ／管理者通知に回して人間の確認に委ねる方針。
    """
    found = check_ng(text)
    if found:
        raise ValueError(f"禁止語を検出しました（{label}）: {', '.join(found)}")
    return text
